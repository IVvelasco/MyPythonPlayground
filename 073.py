""" Exercício retirado do livro "Use a cabeça!- C" dos autores David Griffiths e Dawn Griffits, adaptado para Python """

carta = 'C'

if carta == 'E':
    print("Espadas")
elif carta == 'C':
    print("Copas")
elif carta == 'T':
    print("Trevos")
elif carta == 'P':
    print("Paus")
